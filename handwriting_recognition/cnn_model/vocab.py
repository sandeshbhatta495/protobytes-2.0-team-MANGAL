UNKNOWN_TOKEN = "<unknown>"

# ══════════════════════════════════════════════════════════════════════════
#  Build the canonical vocabulary list  (index → word)
# ══════════════════════════════════════════════════════════════════════════

def build_vocab():
    """Return deduplicated vocabulary list with stable ordering."""
    seen = set()
    vocab = []
    for word in (
        FIRST_NAMES + SURNAMES + MIDDLE_NAMES + RELATIONS +
        PLACES + STATUS_WORDS + ADMIN_TERMS + RELIGION_WORDS +
        DOC_WORDS + DIGITS_NP
    ):
        if word not in seen:
            seen.add(word)
            vocab.append(word)
    # Unknown token always last
    vocab.append(UNKNOWN_TOKEN)
    return vocab


